//switch statement
#include<bits/stdc++.h>
using namespace std;

/* Take the day no and print the corresponding day 
for the 1st print monday
for the 2nd print tuesday and so on for 7th print sunday*/

int main () {

    int day_no;

    cout<< "Please provide the day No";
    cin>>day_no;

    switch(day_no) {

        case 1:
        cout<<"Monday"<<endl;
        break;

        case '2':
        cout<<"Tuesday"<<endl;
        break;

        case '3':
        cout<< "Wednesady"<<endl;
        break;

        case '4':
        cout<<"thrusday"<<endl;
        break;

    }

    




}